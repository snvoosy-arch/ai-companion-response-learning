def build_augment_data() -> list[dict]:
    """모든 Intent의 예문을 합쳐서 학습 데이터 형태로 변환"""
    intent_map = {
        "greeting": GREETING_EXAMPLES,
        "thanks": THANKS_EXAMPLES,
        "hostile": HOSTILE_EXAMPLES,
        "help": HELP_EXAMPLES,
        "who_are_you": WHO_ARE_YOU_EXAMPLES,
        "confirm": CONFIRM_EXAMPLES,
        "deny": DENY_EXAMPLES,
        "why": WHY_EXAMPLES,
        "reply_request": REPLY_REQUEST_EXAMPLES,
        "game_talk": GAME_TALK_EXAMPLES,
        "game_invite": GAME_INVITE_EXAMPLES,
        "music": MUSIC_EXAMPLES,
        "media_recommend": MEDIA_RECOMMEND_EXAMPLES,
        "laugh": LAUGH_EXAMPLES,
        "surprise": SURPRISE_EXAMPLES,
        "tease": TEASE_EXAMPLES,
        "time_date": TIME_DATE_EXAMPLES,
        "news": NEWS_EXAMPLES,
        "search_request": SEARCH_REQUEST_EXAMPLES,
    }

    rows = []
    for intent, examples in intent_map.items():
        seen = set()
        for text in examples:
            normalized = text.strip()
            if normalized and normalized not in seen:
                seen.add(normalized)
                rows.append({
                    "text": normalized,
                    "intent": intent,
                    "source": "manual_augment",
                    "meta": {"auto_labeled": False},
                })

    random.shuffle(rows)
    return rows


def main():
    rows = build_augment_data()

    # 통계
    from collections import Counter
    dist = Counter(r["intent"] for r in rows)

    print(f"생성된 증강 데이터: {len(rows)}개\n")
    for intent, count in dist.most_common():
        print(f"  {intent:25s}: {count:4d}개")

    # 저장
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")

    print(f"\n✅ 저장됨: {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
