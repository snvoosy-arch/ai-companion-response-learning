def category_counts(rows: list[dict[str, Any]]) -> dict[str, int]:
    counter = Counter(str(row.get("meta", {}).get("category") or "uncategorized") for row in rows)
    return dict(counter.most_common())


def main() -> None:
    args = parse_args()
    write_jsonl(args.output, FIXED_ROWS)
    summary = {
        "output": str(args.output),
        "row_count": len(FIXED_ROWS),
        "categories": category_counts(FIXED_ROWS),
        "heads": [*CORE_HEADS, *PLANNER_HEADS],
        "label_counts": label_counts(FIXED_ROWS),
    }
    args.summary_out.parent.mkdir(parents=True, exist_ok=True)
    args.summary_out.write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")
    print(json.dumps(summary, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
